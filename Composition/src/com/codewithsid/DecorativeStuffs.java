package com.codewithsid;

public class DecorativeStuffs {
    private int flowers;
    private int pillows;
    private int curtains;

    public DecorativeStuffs(int flowers, int pillows, int curtains) {
        this.flowers = flowers;
        this.pillows = pillows;
        this.curtains = curtains;
    }

    public int getFlowers() {
        return flowers;
    }

    public int getPillows() {
        return pillows;
    }

    public int getCurtains() {
        return curtains;
    }
}
